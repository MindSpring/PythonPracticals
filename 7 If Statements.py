# 7 - Conditional Statements - If Statements
#================================================
a = 10
b = 20

print('a -- > ',a)
print('b -- > ',b)

print("-------- If Statements -------")
if(a==10):
    print('a == 10')
    
#-------------------------------------------------
    
print("\n---- Nested If Statements ----")
if(a==10):
    print('a == 10')
    if(b==20):
        print('b == 20')
        
#-------------------------------------------------
        
print("\n---- Nested If Statements ----")
if(a==10):
    print('a == 10')
    if(b==30):
        print('b == 20')
